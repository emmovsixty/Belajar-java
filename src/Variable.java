public class Variable {
    public static void main(String[] args) {
        String name; // deklarasi variabel
        name = "hai nama saya farhan mualif";
        Byte umur; // deklarasi variable
        umur = 12;
        System.out.println(name+" umur saya "+umur);
        // kata kunci var
        var fullName = "farhan mualif";
        System.out.println("fullname = "+fullName);

        // kata kunci final
        final var Final = "final UCL 2022 adalah real madrid vs liverpoll";
        System.out.println(Final);
    }
}
