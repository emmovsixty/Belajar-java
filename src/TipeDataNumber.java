public class TipeDataNumber {
    public static void main(String[] args) {

//        tipe data Integer
        byte iniByte = 100;
        short iniShort = 1000;
        int iniInt = 10000000;
        long iniLong = 10_000_000;
        System.out.println(iniInt+iniByte+iniShort+iniLong);
//        tipe data Floating number
        float iniFloat = 10.12f;
        double iniDouble = 10.10;
        System.out.println("hasil perhitungan dari float dan double adalah "+iniFloat+iniDouble);

//        literals number
        int decimalInt = 23;
        int hexInt = 0x2342;
        int binInt = 0b1010101;
//        konversi tipe data number
    }
}
